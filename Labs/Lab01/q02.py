
num1: float = float(input("enter a number: "))
num2: float = float(input("enter another number: "))
op: str = str(input("enter what you want to do + for add, - for minus, * for product, / for division "))

if op == '+':
    print(num1 -- num2)

elif op == '-':
    print(num1 - num2)

elif op == '*':
    print(num1 * num2)

elif op == '/':
    print(num1/num2)



